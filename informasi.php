Penyakit hepatitis merupakan penyakit peradangan hati yang disebabkan oleh virus hepatitis yang menyerang sel-sel hati manusia. Penyakit ini erat kaitannya dengan kurangnya air bersih, sanitasi yang tidak memadai dan kebersihan pribadi yang buruk. Hepatitis ditandai dengan munculnya gejala berupa demam, nyeri sendi, nyeri perut kanan, dan penyakit kuning. Hepatitis dapat bersifat akut (cepat dan tiba-tiba) maupun kronis (perlahan dan bertahap). Jika tidak ditangani dengan baik, hepatitis dapat menimbulkan komplikasi, seperti gagal hati, sirosis, atau kanker hati (hepatocellular carcinoma).<br>

